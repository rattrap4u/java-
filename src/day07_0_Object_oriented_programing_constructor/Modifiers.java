package day07_0_Object_oriented_programing_constructor;
//		we have 12 modifiers
//		out of which 4 are called visibility/access modifiers/specifiers
//		public,private,protected,default/unspecified.
//		
//		-public - accessible everywhere (out of the package)
//		   all method, constructor, classes should be public
//		-protected - accessible within package + child classes anywhere or even in different packages also.
//		  when we use inheritance we go with protected.
//		-private - accessible only within the class restriced to other classes.
//		  all data should be private.
//		-default - means accessible within the package only
